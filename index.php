                <p>&nbsp;</p>
                <p>He  has also been very involved in the community, having served as a member of the  board of the Visalia Chamber of Commerce, Wish Upon a Star and Grace Community  Church. He is involved in the Rotary Club of Visalia and is a graduate of  Leadership Visalia.</p>
                <p>&nbsp;</p>
                <p>Rick  and his wife, Kim, plan to stay in the community, watching their five  granddaughters grow, and he has accepted a teaching position in the Health Care  Administration department of Fresno Pacific University.</p>
